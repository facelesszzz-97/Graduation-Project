 #include "adc.h"
 #include "mq2.h"
 #include "delay.h"				
 /**********************************************************************
 * 函数名称：Adc_Init
 * 功能描述：adc配置初始化
 * 输入参数：无
 * 返回值：无
 * 补充说明：仅以规则通道为例
 **********************************************************************/
void  Adc_Init(void)
{ 	
	ADC_InitTypeDef ADC_InitStructure; 
	GPIO_InitTypeDef GPIO_InitStructure;

	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA |RCC_APB2Periph_ADC1, ENABLE );	  //使能ADC1通道时钟
 

	RCC_ADCCLKConfig(RCC_PCLK2_Div6);   									//设置ADC分频因子6 72M/6=12,ADC最大时间不能超过14M

	//PA0 作为模拟通道输入引脚                         
	GPIO_InitStructure.GPIO_Pin = MQ2_GPIO_IO;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AIN;							//模拟输入引脚
	GPIO_Init(MQ2_GPIO, &GPIO_InitStructure);	

	ADC_DeInit(MQ2_ADC);  //复位ADC1 

	ADC_InitStructure.ADC_Mode = ADC_Mode_Independent;						//ADC工作模式:ADC1工作在独立模式
	ADC_InitStructure.ADC_ScanConvMode = DISABLE;							//模数转换工作在单通道模式
	ADC_InitStructure.ADC_ContinuousConvMode = DISABLE;						//模数转换工作在单次转换模式
	ADC_InitStructure.ADC_ExternalTrigConv = ADC_ExternalTrigConv_None;		//转换由软件而不是外部触发启动
	ADC_InitStructure.ADC_DataAlign = ADC_DataAlign_Right;					//ADC数据右对齐
	ADC_InitStructure.ADC_NbrOfChannel = 1;									//顺序进行规则转换的ADC通道的数目
	ADC_Init(MQ2_ADC, &ADC_InitStructure);									//根据ADC_InitStruct中指定的参数初始化外设ADCx的寄存器   

  
	ADC_Cmd(MQ2_ADC, ENABLE);												//使能指定的ADC1
	
	ADC_ResetCalibration(MQ2_ADC);											//使能复位校准  
	 
	while(ADC_GetResetCalibrationStatus(MQ2_ADC));							//等待复位校准结束
	
	ADC_StartCalibration(MQ2_ADC);	 										//开启AD校准
 
	while(ADC_GetCalibrationStatus(MQ2_ADC));	 							//等待校准结束
 
//	ADC_SoftwareStartConvCmd(ADC1, ENABLE);									//使能指定的ADC1的软件转换启动功能

}				  

/**********************************************************************
* 函数名称：Get_Adc
* 功能描述：获得ADC值
* 输入参数：ch(通道值0~3)
* 返回值：无
* 补充说明：无
**********************************************************************/
u16 Get_Adc(u8 ch)   
{
  	//设置指定ADC的规则组通道，一个序列，采样时间
	ADC_RegularChannelConfig(MQ2_ADC, ch, MQ2_ADC_IO, ADC_SampleTime_239Cycles5 );		//ADC1,ADC通道,采样时间为239.5周期	  			    
  
	ADC_SoftwareStartConvCmd(MQ2_ADC, ENABLE);									//使能指定的ADC1的软件转换启动功能	
	 
	while(!ADC_GetFlagStatus(MQ2_ADC, ADC_FLAG_EOC ));							//等待转换结束

	return ADC_GetConversionValue(MQ2_ADC);									//返回最近一次ADC1规则组的转换结果
}
/**********************************************************************
* 函数名称：Get_Adc_Average
* 功能描述：取多次AD平均值
* 输入参数：ch(通道值0~3)、times(次数)
* 返回值：无
* 补充说明：无
**********************************************************************/
u16 Get_Adc_Average(u8 ch,u8 times)
{
	u32 temp_val=0;
	u8 t;
	for(t=0;t<times;t++)
	{
		temp_val+=Get_Adc(ch);
		delay_ms(5);
	}
	return temp_val/times;
} 	 



























